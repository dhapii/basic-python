a = float(input("Masukkan Angka Pertama: "))
b = float(input("Masukkan Angka Kedua: "))

if a > b:
    print(f"+{a}")  # Jika a lebih besar, tampilkan a dengan tanda +
elif a < b:
    print(f"-{b}")  # Jika b lebih besar, tampilkan b dengan tanda -
else:
    print("Angka sama")  # Jika a dan b sama
